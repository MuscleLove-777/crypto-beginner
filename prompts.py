"""クリプト入門ナビ - プロンプト定義

仮想通貨・DeFi・NFT特化ブログ用のプロンプトを一元管理する。
YMYL×金融対策: 断定表現禁止・免責事項必須・金融庁登録業者優先
投資助言規制対策: 「絶対儲かる」「必ず上がる」等の表現は厳禁
"""


def build_keyword_prompt(config):
    """仮想通貨・DeFi特化キーワード選定プロンプトを構築する"""
    categories_text = "\n".join(f"- {cat}" for cat in config.TARGET_CATEGORIES)
    return (
        "仮想通貨・DeFi・NFT専門ブログ用のキーワードを選定してください。\n\n"
        "以下のカテゴリから1つ選び、そのカテゴリで検索需要が高い"
        "暗号資産関連のキーワードを1つ提案してください。\n\n"
        f"カテゴリ一覧:\n{categories_text}\n\n"
        "【キーワード選定の基準】\n"
        "- ロングテールキーワード（3語以上の組み合わせ）を優先\n"
        "- 初心者が実際に検索しそうなフレーズを意識する\n"
        "- 「ビットコイン ○○ 始め方」「DeFi ○○ やり方」「仮想通貨 ○○ おすすめ」\n"
        "- 「○○取引所 手数料 比較」「NFT ○○ 買い方」「メタマスク ○○ 使い方」\n"
        "- 「仮想通貨 税金 ○○」「暗号資産 確定申告 ○○」等の実用系も重視\n"
        "- トレンド性のあるキーワード（半減期、ETF、新規上場、規制動向など）も考慮\n"
        "- 競合が少なく、かつ検索ボリュームがあるニッチを狙う\n\n"
        "以下の形式でJSON形式のみで回答してください（説明不要）:\n"
        '{"category": "カテゴリ名", "keyword": "キーワード"}'
    )


def build_article_prompt(keyword, category, config):
    """仮想通貨・DeFi記事生成プロンプトを構築する

    YMYL×金融分野のため、免責事項・リスク表記を徹底する設計。
    """
    return f"""あなたは暗号資産アナリスト兼FinTechライターです。
仮想通貨業界で10年以上の経験を持ち、初心者にも複雑な概念を
わかりやすく伝えることに定評があります。

以下のキーワードについて、初心者向けかつ実用的な記事を書いてください。

【基本条件】
- ブログ名: {config.BLOG_NAME}
- キーワード: {keyword}
- カテゴリ: {category}
- 言語: 日本語
- 文字数: {config.MAX_ARTICLE_LENGTH}文字程度

【記事フォーマット】
## この記事の結論
- 結論1（最も重要なポイント）
- 結論2
- 結論3
（※冒頭で結論を示し、忙しい読者でも要点を把握できるようにする）

## 基礎知識
（初心者にもわかりやすく概念を解説する。
専門用語には必ず（）で中学生にもわかる補足説明を付ける。
例：「DeFi（分散型金融 = 銀行を通さず直接お金のやり取りができる仕組み）」
ブロックチェーンの仕組みなど、前提知識が必要な場合は簡潔に補足する）

## 具体的な手順・やり方
（ステップバイステップで解説する。
各ステップに番号を振り、スクリーンショット風の説明を入れる。
例：「ステップ1: ○○取引所の公式サイトにアクセス」
→「画面右上の『口座開設』ボタンをクリック」
手数料・必要書類・所要時間など具体的な数値を入れる）

## メリット・デメリット比較
（表形式で整理する。感情的な表現ではなく、客観的な事実ベースで。
例：手数料率、対応通貨数、セキュリティ体制、日本語対応の有無など
数値で比較できるものは必ず数値を入れる）

## リスクと注意点
（※このセクションは特に重要。以下を必ず含める：
- 価格変動リスク（過去の暴落事例を具体的に）
- ハッキング・詐欺リスク（フィッシング、ラグプルなど）
- 規制リスク（各国の規制動向）
- セキュリティ対策（2段階認証、秘密鍵の管理方法）
- 「投資額は余裕資金の範囲内で」の注記）

## まとめ：初心者が今すぐやるべきこと
（3つ以内の具体的なアクションに絞る。
「まずは○○から始めてみましょう」のように背中を押す表現）

【YMYL・金融対策（最重要・絶対厳守）】
- 「絶対儲かる」「必ず上がる」「損しない」等の断定表現は絶対に使わない
- 「～の可能性がある」「～と言われている」「～が期待されている」を使う
- 「投資は自己責任です。余裕資金の範囲内で行いましょう」の注記を必ず記事末に挿入
- 「本記事は情報提供を目的としており、特定の暗号資産の購入を推奨するものではありません。投資判断はご自身の責任で行ってください」の免責事項を必ず含める
- 金融庁に登録されている国内取引所を優先的に紹介する
- 海外取引所を紹介する場合は「日本の金融庁に未登録の場合がある」旨を明記
- 過去の価格推移に言及する場合「過去の実績は将来のリターンを保証しません」を付記
- 具体的な投資金額の推奨は避ける（「○○円投資すべき」等は禁止）

【SEO要件】
1. タイトルにキーワード「{keyword}」を必ず含めること
2. タイトルは32文字以内で魅力的に
3. H2、H3の見出し構造を適切に使用すること
4. キーワード密度は{config.MIN_KEYWORD_DENSITY}%〜{config.MAX_KEYWORD_DENSITY}%を目安に
5. メタディスクリプションは{config.META_DESCRIPTION_LENGTH}文字以内

【差別化ポイント】
- 初心者目線の丁寧な解説（専門用語には必ず補足）
- 具体的な数値・手数料比較（「○○取引所の売買手数料は○○%」等）
- セキュリティ重視の姿勢（ハードウェアウォレット、2FA、秘密鍵管理など）
- 国内取引所と海外取引所の客観的な比較
- 税金・確定申告の実務的な情報（利益計算方法、申告期限など）
- 内部リンクのプレースホルダーを2〜3箇所に配置（{{{{internal_link:関連トピック}}}}の形式）

【出力形式】
以下のJSON形式で出力してください。JSONブロック以外のテキストは出力しないでください。

```json
{{
  "title": "SEO最適化されたタイトル",
  "content": "# タイトル\\n\\n本文（Markdown形式）...",
  "meta_description": "120文字以内のメタディスクリプション",
  "tags": ["タグ1", "タグ2", "タグ3", "タグ4", "タグ5"],
  "slug": "url-friendly-slug"
}}
```

【注意事項】
- content内のMarkdownは適切にエスケープしてJSON文字列として有効にすること
- tagsは5個ちょうど生成すること
- slugは半角英数字とハイフンのみ使用すること
- contentの末尾に必ず以下の免責事項を含めること：
  「---\\n\\n**免責事項**: 本記事は情報提供を目的としており、特定の暗号資産の購入・売却を推奨するものではありません。暗号資産の取引にはリスクが伴います。投資判断はご自身の責任で行ってください。」
- 金融用語には必ず（カッコ）で平易な補足説明を付けること
- 記事内に少なくとも1箇所「金融庁登録済み」取引所への言及を含めること"""
